#!/usr/bin/perl

use strict;
use warnings;

my %name;

while(<DATA>)
{
	chomp;
	my @a = split(/\t/, $_);
	die "[ERR]$_\n" unless (scalar(@a) >=2);
	$name{$a[0]} = $a[1];
}

my $file = shift || die "usage: $0 data_clean.txt\n";
open(FH, $file) || die $!;
while(<FH>)
{
	chomp;
	my @a = split(/\t/, $_);
	if (defined $name{$a[0]}) {
		$a[1] = $name{$a[0]};
	}
	print join("\t", @a)."\n";
}
close(FH);


